#ifndef ARRAY_H_
#define ARRAY_H_


/**
 * @brief puts values in array in reverse order
 * @param a pointer to array of integers
 * @param b size of array
 */
void reverse(int *array, int size);

/**
 * @brief prints values in an array
 * @param a pointer to array of integers
 * @param b size of array
 */
void print(int *array, int size);

/**
 * @brief swaps 2 values 
 * @param a pointer to first value
 * @param b pointer to second value
 */
void swap(int* a, int* b);

#endif //ARRAY_H_


